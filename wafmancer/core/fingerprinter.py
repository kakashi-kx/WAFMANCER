    def generate_fingerprint_report(self, signature: WAFSignature) -> str:
        """
        Generate a human-readable fingerprint report.

        Args:
            signature: The WAF fingerprint

        Returns:
            Markdown-formatted report
        """
        lines = [
            "=" * 60,
            "WAFMANCER — WAF Fingerprint Report",
            "=" * 60,
            f"Vendor:     {signature.vendor.value}",
            f"Confidence: {signature.confidence:.1%}",
            "",
            "--- Evidence ---",
        ]

        for e in signature.evidence:
            lines.append(f"  ✓ {e}")

        if signature.known_vulnerabilities:
            lines.append("")
            lines.append("--- Known Bypass Vectors ---")
            for v in signature.known_vulnerabilities:
                lines.append(f"  ⚡ {v}")

        if signature.suggested_mutations:
            lines.append("")
            lines.append("--- Suggested Mutations ---")
            for m in signature.suggested_mutations:
                lines.append(f"  🧬 {m}")

        lines.append("=" * 60)
        return "\n".join(lines)
